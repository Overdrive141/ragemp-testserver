let sync = true,
  save = false;

mp.events.add("playerJoin", (player) => {
  player.data.outfit = JSON.parse(JSON.stringify(outfitPresets.default));
  player.resetClothes();
});

mp.events.addCommand("reset", (player) => {
  player.resetClothes();
});

mp.events.addCommand("sync", (player, fullText) => {
  sync = !sync;
  player.outputChatBox(
    `Outfit syncing has been turned ${
      sync === true ? "!{#00ff00}on" : "!{#ff0000}off"
    }`
  );
});

mp.events.addCommand("save", (player, fullText) => {
  save = !save;
  player.outputChatBox(
    `Outfit saving has been turned ${
      save === true ? "!{#00ff00}on" : "!{#ff0000}off"
    }`
  );
});

mp.events.addCommand("outfit", (player, index) => {
  if (
    isNaN(parseInt(index)) ||
    parseInt(index) - 1 >= Object.keys(outfitPresets).length ||
    parseInt(index) - 1 < 0
  )
    return player.outputChatBox(
      `Usage: /outfit [1-${Object.keys(outfitPresets).length}]`
    );

  const outfitPreset =
    outfitPresets[Object.keys(outfitPresets)[parseInt(index) - 1]];
  mp.players.forEach((_player) => {
    for (const componentId in outfitPreset) {
      _player.changeClothes(
        componentId,
        outfitPreset[componentId].drawable,
        outfitPreset[componentId].texture,
        save,
        sync
      );
    }
  });
});
